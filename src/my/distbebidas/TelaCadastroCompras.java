    private void jButtonConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConfirmarActionPerformed
        // TODO add your handling code here:

        String id = jTextFieldId.getText();
        String nome = jTextFieldNome.getText();
        String produto = jTextFieldProduto.getText();
        String data = jTextFieldData.getText();


        /*Verifica se há campos obr. vazios. Se não tiver, faz a inserção no BD.*/
        ComprasDAO compra = new ComprasDAO(id, nome, produto, data);

        if( jTextFieldNome.getText().isEmpty() || jTextFieldId.getText().isEmpty()||
            jTextFieldData.getText().isEmpty() || jTextFieldProduto.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Alguns dos campos obrigatórios estão vazios!");
        }else{
            if(compra.insereCompras()){
                JOptionPane.showMessageDialog(null, "Cadastrado com sucesso!");
                dispose();
            }else{
                System.out.println(compra.insereCompras());
                JOptionPane.showMessageDialog(null, "Houve um erro ao cadastrar!","Erro ao cadastrar",JOptionPane.ERROR_MESSAGE,null);
            }
        }
    }//GEN-LAST:event_jButtonConfirmarActionPerformed

