    private void jButtonCadastrarFornecedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarFornecedorActionPerformed
        // TODO add your handling code here:
        String cnpj = jFormattedTextFieldCNPJ.getText();
        String inscricaoestadual = jFormattedTextFieldInscricaoEstadual.getText();
        String razaosocial = jTextFieldRazaoSocial.getText();
        String nomefantasia = jTextFieldNomeFantasia.getText();
        String endereco = jTextFieldEndereco.getText();      
        String bairro = jTextFieldBairro.getText();
        String cep = jFormattedTextFieldCEP.getText();
        String cidade = jTextFieldCidade.getText();       
        String email = jTextFieldEmail.getText();
        String telefone = jFormattedTextFieldTelefone.getText();
        String celular = jFormattedTextFieldCelular.getText();
        String uf = (String) jComboBoxEstado.getSelectedItem();
        
            /*Verifica se há campos obr. vazios. Se não tiver, faz a inserção no BD.*/
            FornecedorDAO fornece = new FornecedorDAO(cnpj, inscricaoestadual, razaosocial, nomefantasia, endereco, bairro, cep, cidade,
                    email, telefone, celular, uf);
       
                        
            if( (!jFormattedTextFieldCNPJ.getText().equals(jFormattedTextFieldCNPJ.getValue())) || jTextFieldNomeFantasia.getText().isEmpty() || jTextFieldRazaoSocial.getText().isEmpty()||
                    jFormattedTextFieldCEP.getText().isEmpty() ||  (!jFormattedTextFieldTelefone.getText().equals(jFormattedTextFieldTelefone.getValue()))){
                JOptionPane.showMessageDialog(null,"Alguns dos campos obrigatórios estão vazios!");
            }else{
                if(fornece.insereFornecedor()==1){
                    JOptionPane.showMessageDialog(null, "Cadastrado com sucesso!");
                    dispose();
                    new DistBebidasUI().setVisible(true);
                }else{
                    System.out.println(fornece.insereFornecedor());
                    JOptionPane.showMessageDialog(null, "Houve um erro ao cadastrar!","Erro ao cadastrar",JOptionPane.ERROR_MESSAGE,null);
                }
            }
    }//GEN-LAST:event_jButtonCadastrarFornecedorActionPerformed

